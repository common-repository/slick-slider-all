<div class="cs-slick-slider-all-wrapper">
	<h3>Simple shortcode</h3>
	<p>[cs_slick_slider_all post_type="post" post_number="10"]</p>
	
	<h3>Available attributes</h3>
	<p><strong>post_type</strong> - select post type</p>
	<p><strong>post_number</strong> - put the number of the articles you want to show</p>
	<p><strong>taxonomy</strong> - define taxonomy</p>
	<p><strong>terms</strong> - put terms id separated by comma</p>
	<p><strong>orderby</strong> - default: date</p>
	<p><strong>order</strong> - default: DESC</p>
	<p><strong>show_description</strong> - true or false</p>
	<p><strong>swipe</strong> - true or false (determine will the slider be touchable)</p>
	<p><strong>responsive</strong> - default: large=4,medium=3,small=2,exsmall=1</p>
	
	<h3>Examples:</h3>
	<p>[cs_slick_slider_all post_type="post" post_number="10" swipe="false" responsive="large=5,medium=3,small=1,exsmall=1" show_description="true"]</p>
	
	<p>[cs_slick_slider_all post_type="project" taxonomy="project_cat" terms="10,14,16" post_number="10" swipe="false" ]</p>

</div>